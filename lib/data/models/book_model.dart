class Book {
  final String id;
  final String title;
  final String author;
  final String coverUrl;
  final String description;
  final int price;
  final double rating;

  Book({
    required this.id,
    required this.title,
    required this.author,
    required this.coverUrl,
    required this.description,
    required this.price,
    required this.rating,
  });
}
